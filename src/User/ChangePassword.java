     public boolean verifyFields()
    {
        String pass= String.valueOf(jPasswordField1.getPassword());
        String pass1 = String.valueOf(jPasswordField2.getPassword());
        String pass2 = String.valueOf(jPasswordField3.getPassword());        
        // check empty fields
        if(pass1.trim().equals("") || pass2.trim().equals("")||pass.trim().equals(""))
        {
            JOptionPane.showMessageDialog(this,"One or more empty fields");
            return false;
        }
          // check if the two password are equals or not
        else if(!pass1.equals(pass2))
        {
           JOptionPane.showMessageDialog(this,"New password has different values in both fields");
           return false;
        }
        
        // if everything is ok
        else{
            return true;
        }
    }
